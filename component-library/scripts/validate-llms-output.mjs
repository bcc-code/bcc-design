import { readFile } from 'node:fs/promises';
import path from 'node:path';

function docsRoute(id) {
	return `/docs/${id}`;
}

function stripCodeFences(contents) {
	return String(contents ?? '').replace(/```[\s\S]*?```/g, '');
}

function markdownLinks(contents) {
	const links = [];
	const linkPattern = /!?\[[^\]]*\]\(([^)\n]+)\)/g;

	for (const match of contents.matchAll(linkPattern)) {
		const target = match[1]?.trim();
		if (target) links.push(target);
	}

	return links;
}

function normalizeInternalPath(rawTarget, sourcePath, baseUrl) {
	let target = rawTarget.trim();
	if (!target || target.startsWith('#')) return '';

	if (target.startsWith('<') && target.endsWith('>')) {
		target = target.slice(1, -1).trim();
	}

	if (!target) return '';

	// Keep only URL/token part when a markdown title is present: (/docs/x.md "Title")
	if (target.includes(' ')) {
		target = target.split(/\s+/, 1)[0];
	}

	if (target.startsWith('/docs/') || target === '/llms.txt' || target === '/llms-full.txt') {
		return target.startsWith('/docs/') && !target.endsWith('.md') ? `${target}.md` : target;
	}

	if (
		/^https?:\/\//i.test(target) &&
		!target.includes('/docs/') &&
		!target.endsWith('/llms.txt') &&
		!target.endsWith('/llms-full.txt')
	) {
		return '';
	}

	if (/^(mailto:|tel:|data:|javascript:)/i.test(target)) return '';

	const base = new URL(baseUrl);
	const normalizePathFromUrl = url => {
		let route = url.pathname;

		if (route === '/iframe.html' && url.searchParams.get('viewMode') === 'docs' && url.searchParams.get('id')) {
			route = docsRoute(url.searchParams.get('id'));
		}

		if (url.searchParams.has('path')) {
			const pathRoute = url.searchParams.get('path');
			if (pathRoute?.startsWith('/docs/')) {
				route = pathRoute;
			}
		}

		if (route.startsWith('/docs/') && !route.endsWith('.md')) {
			route = `${route}.md`;
		}

		if (!(route.startsWith('/docs/') || route === '/llms.txt' || route === '/llms-full.txt')) {
			return '';
		}

		return route;
	};

	if (/^[a-z][a-z0-9+.-]*:/i.test(target)) {
		let parsed;
		try {
			parsed = new URL(target);
		} catch {
			return '';
		}

		if (parsed.origin !== base.origin) return '';
		return normalizePathFromUrl(parsed);
	}

	if (target.startsWith('//')) return '';

	let resolved;
	if (target.startsWith('/')) {
		resolved = target;
	} else {
		const sourceDir = path.posix.dirname(sourcePath);
		resolved = path.posix.resolve(sourceDir, target);
	}

	let parsed;
	try {
		parsed = new URL(resolved, base.origin);
	} catch {
		return '';
	}

	return normalizePathFromUrl(parsed);
}

function uniqueSorted(values) {
	return Array.from(new Set(values)).sort((left, right) => left.localeCompare(right));
}

function collectDuplicateByKey(values, getKey) {
	const occurrences = new Map();

	for (const value of values) {
		const key = getKey(value);
		const bucket = occurrences.get(key) ?? [];
		bucket.push(value);
		occurrences.set(key, bucket);
	}

	return Array.from(occurrences.entries())
		.filter(([, bucket]) => bucket.length > 1)
		.map(([key, bucket]) => ({ key, values: bucket }));
}

export async function validateGeneratedOutputs({ outputDir, pages, baseUrl, llmsTxt, llmsFullTxt, sectionBuckets }) {
	const issues = [];
	const availableInternalPaths = new Set(['/llms.txt', '/llms-full.txt', ...pages.map(page => page.markdownRoute)]);

	const duplicateIds = collectDuplicateByKey(pages, page => page.id);
	for (const duplicate of duplicateIds) {
		issues.push(
			`Duplicate docs ID \`${duplicate.key}\` appears ${duplicate.values.length} times (${duplicate.values
				.map(page => page.title)
				.join(', ')}).`
		);
	}

	const startHereBucket = sectionBuckets.find(bucket => bucket.name === 'Start here');
	if (!startHereBucket?.labels.length) {
		issues.push(
			'Missing Start here entries. Mark at least one docs page as Readme, AI Tools, or Foundations/Overview.'
		);
	}

	for (const page of pages) {
		if (!page.summary?.trim()) {
			issues.push(`Empty summary for ${page.id}.`);
		}

		if (/\.\.\.\s*$/.test(page.summary ?? '')) {
			issues.push(`Summary ends with ellipsis for ${page.id}.`);
		}
	}

	if (llmsTxt.includes('Autogenerated Storybook documentation')) {
		issues.push(
			'llms.txt contains literal "Autogenerated Storybook documentation". Replace autogenerated fallback summaries.'
		);
	}

	const unresolvedTextPattern = /\bunresolved\b/i;
	const unresolvedPlaceholderPattern = /\{\{\s*[A-Z0-9_.-]{2,}\s*\}\}/;
	const llmsTxtWithoutCode = stripCodeFences(llmsTxt);
	const llmsFullWithoutCode = stripCodeFences(llmsFullTxt);
	if (
		unresolvedTextPattern.test(llmsTxtWithoutCode) ||
		unresolvedTextPattern.test(llmsFullWithoutCode) ||
		unresolvedPlaceholderPattern.test(llmsTxtWithoutCode) ||
		unresolvedPlaceholderPattern.test(llmsFullWithoutCode)
	) {
		issues.push('Detected unresolved tokens in llms output.');
	}

	for (const bucket of sectionBuckets) {
		const duplicates = collectDuplicateByKey(bucket.labels, label => label.toLowerCase());
		for (const duplicate of duplicates) {
			issues.push(`Duplicate llms label in section \`${bucket.name}\`: \`${duplicate.values[0]}\`.`);
		}
	}

	const linkSources = [
		{ name: 'llms.txt', sourcePath: '/llms.txt', contents: llmsTxt },
		{ name: 'llms-full.txt', sourcePath: '/llms-full.txt', contents: llmsFullTxt },
		...pages.map(page => ({
			name: `${page.id}.md`,
			sourcePath: page.markdownRoute,
			contents: page.markdown,
		})),
	];

	for (const source of linkSources) {
		const brokenTargets = [];

		for (const rawTarget of markdownLinks(stripCodeFences(source.contents))) {
			const normalized = normalizeInternalPath(rawTarget, source.sourcePath, baseUrl);
			if (!normalized) continue;
			if (!availableInternalPaths.has(normalized)) {
				brokenTargets.push(`${rawTarget} -> ${normalized}`);
			}
		}

		for (const broken of uniqueSorted(brokenTargets)) {
			issues.push(`Broken internal link in ${source.name}: ${broken}`);
		}
	}

	const llmsFullPath = path.join(outputDir, 'llms-full.txt');
	const llmsFullExists = await readFile(llmsFullPath, 'utf8')
		.then(() => true)
		.catch(() => false);
	if (!llmsFullExists) {
		issues.push('Missing /llms-full.txt output file.');
	}

	if (issues.length > 0) {
		throw new Error(`LLMS quality check failed:\n- ${issues.join('\n- ')}`);
	}
}
